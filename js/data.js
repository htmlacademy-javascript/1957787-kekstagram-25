const MIN_LIKES = 15;
const MAX_LIKES = 200;

const MIN_AVATAR_ID = 1;
const MAX_AVATAR_ID = 6;

const MAX_OBJECTS = 25;

const uniqueID = (function() {
  let id = 0;
  return function() { return id++; };
})();

// ะคัะฝะบัะธั ะฒะพะทะฒัะฐัะฐะตั ะพะดะธะฝ ะพะฑัะตะบั ะบะพะผะผะตะฝััะฐัะธั
function createComment () {
  const comment = {
    id: uniqueID(),
    avatar: `img/avatar-${getRandomPositiveInteger(MIN_AVATAR_ID, MAX_AVATAR_ID)}.svg`,
    message: getRandomArrayElement(MESSAGES),
    name: getRandomArrayElement(NAMES)
  };
  return comment;
}

// ะกะพะทะดะฐะตะผ ะผะฐััะธะฒ ะธะท ะพะฑัะตะบัะพะฒ ะบะพะผะผะตะฝัะฐัะธะตะฒ
function createComments(countComments) {
  const comments = [];
  for (let i = 0; i < countComments; i++) {
    comments.push(createComment());
  }
  return comments;
}

function generatePhotos(countPhoto) {
  const photos = [];
  for (let i = 0; i < countPhoto; i++) {
    photos.push({
      id: i + 1,
      url: `photos/${i + 1}.jpg`,
      description: getRandomArrayElement(DESCRIPTIONS),
      likes: getRandomPositiveInteger(MIN_LIKES, MAX_LIKES),
      comments: createComments(getRandomPositiveInteger(1, 4))
    });
  }
  return photos;
}

const multipleObject = () => generatePhotos(MAX_OBJECTS);

export { multipleObject };
